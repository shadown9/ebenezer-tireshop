export type AdminTextKey = keyof typeof adminText.en

export function useAdminText() {
  const { language } = useLanguageStore()
  const text = adminText[language]
  const at = (key: AdminTextKey) => text[key] || adminText.en[key] || key

  return { at, language }
}
